/**
 * Operadores de comparação
 *
 * > maior que
 * >= maior que ou igual a
 * < menor que
 * <= menor que ou igual a
 * == igualdade (valor)
 * === igualdae estrita (valor e tipo)
 * != diferente (valor)
 * !== diferente estrito (valor e tipo)
 */

console.log(10 > 5);

const num1 = 10;
const num2 = "10";
console.log(num1 == num2);
console.log(num1 === num2);

console.log(num1 != num2);
console.log(num1 !== num2);
